# Concatenating
text = "dog" + "cat"
print(text)

word1 = "Good"
word2 = "Day"
word3 = "John"

sentence = word1 + " " + word2 + " " + word3

print(sentence)

# F-Strings

age = 25
print(f"John is {age} years old")

print(f"John will be {age+10} years old in 10 years!")

# Escape Characters

text2 = "Hello, they call me \"Jeff\""
print(text2)

